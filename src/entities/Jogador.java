package entities;

import java.util.Date;

public class Jogador {
	
	private String nome;
	private Date nascimento;
	private char genero;
	private Double altura;
	
	public Jogador() {
		
	}

	public String getNome() {
		return nome;
	}

	public Date getNascimento() {
		return nascimento;
	}

	public char getGenero() {
		return genero;
	}

	public Double getAltura() {
		return altura;
	}
	
	

}
